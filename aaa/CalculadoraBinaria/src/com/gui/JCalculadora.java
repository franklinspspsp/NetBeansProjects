    private void jconvertirAKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jconvertirAKeyReleased
        // TODO add your handling code here:
        String texto = jconvertirA.getText();
        char[] fuente = texto.toCharArray();
        char[] resultado = new char[fuente.length];
        int j = 0;
        boolean error = false;
        if (jComboBox2.getSelectedItem().equals("Binario a Decimal") || jComboBox2.getSelectedItem().equals("Binario a Hexadecimal")
                || jComboBox2.getSelectedItem().equals("Binario a Octal")) {
            for (int i = 0; i < fuente.length; i++) {

                if (fuente[i] >= '0' && fuente[i] <= '1') {
                    resultado[j++] = fuente[i];
                } else {
                    error = true;
                    java.awt.Toolkit.getDefaultToolkit().beep();
                }
            }
        }
        if (jComboBox2.getSelectedItem().equals("Octal a Decimal") || jComboBox2.getSelectedItem().equals("Octal a Binario")) {
            for (int i = 0; i < fuente.length; i++) {

                if (fuente[i] >= '0' && fuente[i] <= '7') {
                    resultado[j++] = fuente[i];
                } else {
                    error = true;
                    java.awt.Toolkit.getDefaultToolkit().beep();
                }
            }
        }
        if (jComboBox2.getSelectedItem().equals("Hexadecimal a Decimal") || jComboBox2.getSelectedItem().equals("Hexadecimal a Binario")) {
            for (int i = 0; i < fuente.length; i++) {

                if ((fuente[i] >= '0' && fuente[i] <= '9') || fuente[i] == 'A'
                        || fuente[i] == 'B' || fuente[i] == 'C' || fuente[i] == 'D'
                        || fuente[i] == 'E' || fuente[i] == 'F') {
                    resultado[j++] = fuente[i];
                } else {
                    error = true;
                    java.awt.Toolkit.getDefaultToolkit().beep();
                }
            }
        }
        if (jComboBox2.getSelectedItem().equals("Decimal a Binario")
                || jComboBox2.getSelectedItem().equals("Decimal a Octal")
                || jComboBox2.getSelectedItem().equals("Decimal a Hexadecimal")) {
            for (int i = 0; i < fuente.length; i++) {

                if (fuente[i] >= '0' && fuente[i] <= '9') {
                    resultado[j++] = fuente[i];
                } else {
                    error = true;
                    java.awt.Toolkit.getDefaultToolkit().beep();
                }
            }
        }
        if (error) {
            jconvertirA.setText(new String(resultado, 0, j));
        }
    }//GEN-LAST:event_jconvertirAKeyReleased

