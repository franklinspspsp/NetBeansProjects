    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        if (jComboBox1.getSelectedItem().equals("Suma")) {
            tbxNumRes.setText(OpBinaria.Suma(tbxNumA.getText(), tbxNumB.getText()));
        }
        if (jComboBox1.getSelectedItem().equals("Resta")) {
            if(Integer.parseInt(tbxNumA.getText(), 2) < Integer.parseInt(tbxNumB.getText(), 2)){
                 JOptionPane.showMessageDialog(null, "Para esta resta debe usar complemento A1", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                 CleanFields();
            }else{
                tbxNumRes.setText(OpBinaria.Resta(tbxNumA.getText(), tbxNumB.getText()));
            }
        }
        if (jComboBox1.getSelectedItem().equals("División") && !tbxNumB.getText().equals("0")) {
            tbxNumRes.setText(OpBinaria.Divicion(tbxNumA.getText(), tbxNumB.getText()));
        }
        if (jComboBox1.getSelectedItem().equals("Multiplicación")) {
            tbxNumRes.setText(OpBinaria.Multiplicacion(tbxNumA.getText(), tbxNumB.getText()));
        }
         } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tbxNumAKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbxNumAKeyReleased
        // TODO add your handling code here:
        String texto = tbxNumA.getText();
        char[] fuente = texto.toCharArray();
        char[] resultado = new char[fuente.length];
        int j = 0;
        boolean error = false;
        for (int i = 0; i < fuente.length; i++) {

            if (fuente[i] >= '0' && fuente[i] <= '1') {
                resultado[j++] = fuente[i];
            } else {
                error = true;
                java.awt.Toolkit.getDefaultToolkit().beep();
            }
        }
        if (error) {
            tbxNumA.setText(new String(resultado, 0, j));
        }
    }//GEN-LAST:event_tbxNumAKeyReleased

